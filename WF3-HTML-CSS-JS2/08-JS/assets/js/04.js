/* --------------------------------------------
        Les Opérateurs Arithmétique
---------------------------------------------*/

//  ~ ~ L'Addition ~ ~ //

// -- Déclaration de plusieurs variables a la suite 
var nb1, nb2, resultat;

nb1 = 10;
nb2 = 5 ;

// -- Addition de nb1 + nb2 avec l'opérateur "+"
resultat = nb1 + nb2;
console.log("L'Addition de nb1 et nb2 est egale à : " + resultat);


//  ~ ~ La Soustraction ~ ~ //
// -- Addition de nb1 - nb2 avec l'opérateur "-"
resultat = nb1 - nb2;
console.log("La Soustraction de nb1 et nb2 est egal à : " + resultat)  

//  ~ ~ La Multiplication ~ ~ //
// -- La multiplication de nb1 * nb2 avec l'opérateur "*"
resultat = nb1 * nb2;
console.log("La multiplication de nb1 et nb2 est egal à : "+ resultat);  // -- idem pour division 

//  ~ ~ Le Modulo ~ ~ //

// -- Le résultat du modulo est le reste de la division.
    // -- Le modulo de nb1 et nb2 avec l'operateur %
nb1 = 11 
resultat = nb1 % nb2 ;
console.log("Le modulo de nb1 et nb2 est égale à : " + resultat)

